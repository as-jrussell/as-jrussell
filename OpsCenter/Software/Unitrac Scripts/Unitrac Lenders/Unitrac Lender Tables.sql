FROM ADDRESS
WHERE ID IN (11912,11913)

--Catch All Code Storage
SELECT *
FROM REF_CODE

--Catch All Code Storage Everything Data
FROM RELATED_DATA_DEF

--Addtl Data screen in Lender
SELECT RELATED_DATA_DEF.NAME_TX,RELATED_DATA_DEF.DESC_TX,RELATED_DATA.VALUE_TX,*
FROM RELATED_DATA
INNER JOIN RELATED_DATA_DEF ON RELATED_DATA.DEF_ID = RELATED_DATA_DEF.ID
WHERE RELATED_DATA.RELATE_ID = 825
ORDER BY RELATED_DATA_DEF.DESC_TX

--Organization Tab
SELECT LENDER_ORGANIZATION.*
FROM LENDER 
INNER JOIN LENDER_ORGANIZATION ON LENDER.ID = LENDER_ORGANIZATION.LENDER_ID
WHERE LENDER.CODE_tX = '2046'

--CONTRACT TYPES by Lender
SELECT LENDER.CODE_TX,LENDER.NAME_TX,LENDER_ORGANIZATION.*
FROM LENDER 
INNER JOIN LENDER_ORGANIZATION ON LENDER.ID = LENDER_ORGANIZATION.LENDER_ID AND LENDER_ORGANIZATION.TYPE_CD = 'DIV'
ORDER BY LENDER.CODE_TX

--All Available Division Codes
SELECT DISTINCT NAME_TX
FROM LENDER_ORGANIZATION
WHERE TYPE_CD = 'DIV'


--Collateral Code Tab
