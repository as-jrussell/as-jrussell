--Master Policy Tab
SELECT *
FROM MASTER_POLICY
INNER JOIN CARRIER ON MASTER_POLICY.CARRIER_ID = CARRIER.ID
INNER JOIN CARRIER_PRODUCT ON MASTER_POLICY.CARRIER_PRODUCT_ID = CARRIER_PRODUCT.ID
INNER JOIN MASTER_POLICY_ASSIGNMENT ON MASTER_POLICY.ID = MASTER_POLICY_ASSIGNMENT.MASTER_POLICY_ID
WHERE LENDER_ID = 825

SELECT *
FROM MASTER_POLICY_ENDORSEMENT
INNER JOIN POLICY_ENDORSEMENT ON MASTER_POLICY_ENDORSEMENT.POLICY_ENDORSEMENT_ID = POLICY_ENDORSEMENT.ID
WHERE MASTER_POLICY_ASSIGNMENT_ID IN (13465,13466,13467,13468,13469,13470,13471,13472,13473,13474,13475,13476,13477,13478,13479,13480,13481,13482,13483)


--Products Tab
