SELECT DISTINCT L.CODE_TX, L.NAME_TX, EVENT_TYPE_CD, ESC.DESCRIPTION_TX, LP.DESCRIPTION_TX, A.NAME_TX--, * 
FROM dbo.EVENT_SEQUENCE ES
JOIN dbo.EVENT_SEQ_CONTAINER ESC ON ESC.ID = ES.EVENT_SEQ_CONTAINER_ID
JOIN dbo.LENDER L ON L.ID = ESC.LENDER_ID
JOIN dbo.LENDER_PRODUCT LP ON LP.LENDER_ID = L.ID
JOIN dbo.AGENCY A ON A.ID = L.AGENCY_ID
WHERE ESC.DESCRIPTION_TX IN ('High Deductible', 'Lienholder Impaired') AND EVENT_TYPE_CD = 'ISCT'
AND ES.PURGE_DT IS NULL AND ESC.PURGE_DT IS NULL AND L.PURGE_DT IS NULL AND L.TEST_IN = 'N'
AND L.STATUS_CD = 'ACTIVE' AND AGENCY_ID = '1' AND LP.DESCRIPTION_TX LIKE '%Vehicle%'
ORDER BY L.CODE_TX ASC 




