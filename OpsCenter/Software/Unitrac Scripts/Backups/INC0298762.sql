--Loan Screen Information - LOAN/COLLATERAL/PROPERTY/OWNER/REQUIRED COVERAGE
SELECT L.NUMBER_TX [Loan Number],P.DESCRIPTION_TX,
        P.YEAR_TX [Year] ,
        P.MAKE_TX [Make],
        P.MODEL_TX [Mode] ,
        P.BODY_TX [Body],
        P.VIN_TX [VIn], 
		CC1.CODE_TX [Old Collateral],
		CC2.CODE_TX [New Collateral],
		PC.USER_TX [User], 
		PCU.CREATE_DT [Date]
 FROM LOAN L
INNER JOIN COLLATERAL C ON L.ID = C.LOAN_ID
INNER JOIN dbo.PROPERTY P ON P.ID = C.PROPERTY_ID
INNER JOIN dbo.LENDER LL ON LL.ID = L.LENDER_ID
INNER JOIN dbo.PROPERTY_CHANGE PC ON PC.ENTITY_ID = C.ID AND PC.ENTITY_NAME_TX = 'Allied.UniTrac.Collateral'
LEFT JOIN dbo.PROPERTY_CHANGE_UPDATE PCU ON PC.ID = PCU.CHANGE_ID
INNER JOIN dbo.COLLATERAL_CODE CC1 ON CC1.ID = PCU.FROM_VALUE_TX
INNER JOIN dbo.COLLATERAL_CODE CC2 ON CC2.ID = PCU.TO_VALUE_TX
WHERE LL.CODE_TX IN ('4500') AND c.UPDATE_DT >= '2016-09-16' AND PCU.COLUMN_NM = 'COLLATERAL_CODE_ID'
