SELECT DISTINCT L.NUMBER_TX , RC.DESCRIPTION_TX [Loan Status],
RCC.DESCRIPTION_TX [Record Type], RCD.DESCRIPTION_TX [Loan Type],
LO.NAME_TX [Loan Division]
     
INTO jcs..INC0276090
FROM dbo.LOAN L 
JOIN dbo.REF_CODE RC ON RC.CODE_CD = L.STATUS_CD AND RC.DOMAIN_CD = 'LoanStatus'
JOIN dbo.REF_CODE RCC ON RCC.CODE_CD = L.RECORD_TYPE_CD AND RCC.DOMAIN_CD = 'RecordType'
JOIN dbo.REF_CODE RCD ON RCD.CODE_CD = L.TYPE_CD AND RCD.DOMAIN_CD = 'LoanType'
JOIN dbo.LENDER_ORGANIZATION LO ON LO.CODE_TX = L.DIVISION_CODE_TX AND LO.LENDER_ID = L.LENDER_ID
JOIN dbo.LENDEr LL ON LL.ID = L.LENDER_ID
WHERE LL.CODE_TX = '2252' AND L.NUMBER_TX LIKE '%-%'
AND L.PURGE_DT IS NULL AND L.RECORD_TYPE_CD IN ('G', 'A') AND L.STATUS_CD  = 'A'



