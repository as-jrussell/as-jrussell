--Loan Screen Information - LOAN/COLLATERAL/PROPERTY/OWNER/REQUIRED COVERAGE
SELECT L.NUMBER_TX, IH.* INTO UniTracHDStorage..INC0310108_IH
 FROM LOAN L
INNER JOIN COLLATERAL C ON L.ID = C.LOAN_ID
INNER JOIN PROPERTY P ON C.PROPERTY_ID = P.ID
INNER JOIN REQUIRED_COVERAGE RC ON P.ID = RC.PROPERTY_ID
INNER JOIN dbo.INTERACTION_HISTORY IH ON ih.PROPERTY_ID = p.id
WHERE L.ID IN (SELECT RELATE_ID FROM dbo.PROCESS_LOG_ITEM
WHERE PROCESS_LOG_ID IN (47552194,47552308)
AND RELATE_TYPE_CD = 'Allied.UniTrac.Loan'
AND RELATE_ID <> '0') AND IH.UPDATE_USER_TX = 'LDHPCRA'


