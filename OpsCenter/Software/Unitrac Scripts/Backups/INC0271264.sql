USE UniTrac	


SELECT P.REPLACEMENT_COST_VALUE_NO , T.PropertyRCV, P.* 
--INTO UniTracHDStorage..INC0271264_P
FROM dbo.LOAN L
JOIN dbo.COLLATERAL C ON C.LOAN_ID = L.ID
JOIN dbo.PROPERTY P ON P.ID = C.PROPERTY_ID
JOIN dbo.LENDER LL ON LL.ID = L.LENDER_ID
JOIN UniTracHDStorage..VermontECU T ON T.[LoanNumber] = L.NUMBER_TX
WHERE LL.CODE_TX = '3141' AND L.NUMBER_TX = '99991782'



SELECT * FROM UniTracHDStorage..VermontECU



UPDATE P
SET P.REPLACEMENT_COST_VALUE_NO = 0, P.Update_DT= GETDATE(), P.UPDATE_USER_TX = 'INC0271264',P.LOCK_ID = P.LOCK_ID+1
--SELECT P.REPLACEMENT_COST_VALUE_NO,* 
FROM dbo.LOAN L
JOIN dbo.COLLATERAL C ON C.LOAN_ID = L.ID
JOIN dbo.PROPERTY P ON P.ID = C.PROPERTY_ID
JOIN dbo.LENDER LL ON LL.ID = L.LENDER_ID
JOIN UniTracHDStorage..VermontECU T ON T.[LoanNumber] = L.NUMBER_TX
WHERE LL.CODE_TX = '3141'




