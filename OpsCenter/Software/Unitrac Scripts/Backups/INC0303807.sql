USE UniTrac


--DROP TABLE #tmpLender
SELECT L.ID INTO #tmpLender FROM dbo.LENDER L
JOIN dbo.ADDRESS A ON L.PHYSICAL_ADDRESS_ID = A.ID
WHERE A.STATE_PROV_TX = 'KY'

--DROP TABLE #tmpLoan
SELECT ID into #tmpLoan FROM dbo.LOAN
WHERE LENDER_ID IN (SELECT * FROM #tmpLender) AND DIVISION_CODE_TX= '3'

--DROP TABLE #tmpProperty
SELECT P.ID INTO #tmpProperty FROM dbo.COLLATERAL C 
JOIN dbo.PROPERTY P ON P.ID = C.PROPERTY_ID
WHERE C.LOAN_ID IN (SELECT * FROM #tmpLoan)

--DROP TABLE #tmpFPC
SELECT RELATE_ID INTO #tmpFPC FROM dbo.INTERACTION_HISTORY 
WHERE PROPERTY_ID IN (SELECT * FROM #tmpProperty)
AND TYPE_CD = 'CPI' AND RELATE_CLASS_TX = 'Allied.UniTrac.ForcePlacedCertificate'

--DROP TABLE #tmpCarrier
SELECT ID INTO #tmpCarrier FROM dbo.CARRIER
WHERE NAME_TX LIKE '%securian%'







SELECT DISTINCT concat(LL.CODE_TX,' ',LL.NAME_TX ) [Lender]
   
FROM    LOAN L
        INNER JOIN COLLATERAL C ON L.ID = C.LOAN_ID
        INNER JOIN PROPERTY P ON C.PROPERTY_ID = P.ID
        INNER JOIN REQUIRED_COVERAGE RC ON P.ID = RC.PROPERTY_ID
        INNER JOIN OWNER_LOAN_RELATE OL ON L.ID = OL.LOAN_ID
        INNER JOIN OWNER O ON OL.OWNER_ID = O.ID
        INNER JOIN OWNER_ADDRESS OA ON O.ADDRESS_ID = OA.ID
        INNER JOIN dbo.LENDER LL ON LL.ID = L.LENDER_ID
        INNER JOIN dbo.LENDER_PRODUCT LP ON LP.ID = RC.LENDER_PRODUCT_ID
		INNER JOIN dbo.FORCE_PLACED_CERTIFICATE FPC ON FPC.LOAN_ID = L.ID
		INNER JOIN dbo.CARRIER CC ON CC.ID = FPC.CARRIER_ID
        INNER JOIN dbo.REF_CODE RC1 ON RC1.CODE_CD = RC.STATUS_CD
                                       AND RC1.DOMAIN_CD = 'RequiredCoverageStatus'
        INNER JOIN dbo.REF_CODE RC2 ON RC2.CODE_CD = L.RECORD_TYPE_CD
                                       AND RC2.DOMAIN_CD = 'RecordType'
        INNER JOIN dbo.REF_CODE RC3 ON RC3.CODE_CD = L.STATUS_CD
                                       AND RC3.DOMAIN_CD = 'LoanStatus'
        LEFT JOIN dbo.REF_CODE RC4 ON RC4.CODE_CD = L.TYPE_CD
                                       AND RC4.DOMAIN_CD = 'LoanType'
        LEFT JOIN dbo.REF_CODE RC5 ON RC5.CODE_CD = RC.INSURANCE_STATUS_CD
                                       AND RC5.DOMAIN_CD = 'RequiredCoverageInsStatus'
        LEFT JOIN dbo.REF_CODE RC6 ON RC6.CODE_CD = RC.INSURANCE_SUB_STATUS_CD
                                       AND RC6.DOMAIN_CD = 'RequiredCoverageInsSubStatus'
        LEFT JOIN dbo.REF_CODE RC7 ON RC7.CODE_CD = RC.SUB_STATUS_CD
                                       AND RC7.DOMAIN_CD = 'RequiredCoverageSubStatus'
		LEFT JOIN dbo.LENDER_ORGANIZATION LO ON LO.LENDER_ID = LL.ID 
									    AND LO.TYPE_CD = 'DIV' AND LO.CODE_TX = L.DIVISION_CODE_TX	
				WHERE L.ID IN (SELECT LOAN_ID FROM dbo.FORCE_PLACED_CERTIFICATE
WHERE ID IN (SELECT * FROM #tmpFPC) AND  CARRIER_ID IN (SELECT * FROM #tmpCarrier))	
AND CC.NAME_TX LIKE '%securian%'	
