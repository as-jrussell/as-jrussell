SELECT L.NUMBER_TX, C.ID, PCU.COLUMN_NM, PCU.FROM_VALUE_TX, PCU.TO_VALUE_TX INTO #tmpPC FROM dbo.PROPERTY_CHANGE PC
LEFT JOIN dbo.PROPERTY_CHANGE_UPDATE PCU ON PC.ID = PCU.CHANGE_ID 
INNER JOIN COLLATERAL C ON C.ID = PC.ENTITY_ID AND ENTITY_NAME_TX = 'Allied.UniTrac.Collateral'
INNER JOIN dbo.LOAN L ON L.ID = C.LOAN_ID
WHERE ENTITY_ID IN (SELECT * FROM #tmp) 
AND PCU.COLUMN_NM = 'COLLATERAL_CODE_ID' AND PCU.CREATE_DT >= '2016-10-28 '

SELECT * FROM #tmpPC


UPDATE C
SET C.UPDATE_DT = GETDATE(), C.UPDATE_USER_TX = 'INC0271430', C.LOCK_ID = C.LOCK_ID+1,
