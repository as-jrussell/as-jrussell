USE UniTrac

SELECT L.NAME_TX [Lender Name], PE.NAME_TX [Name], PE.PERCENTAGE_NO [Percentage],  MPE.START_DT [Start], MPE.END_DT [End]
INTO JCS..INC0323385
--SELECT MP.IN_USE, pe.IN_USE_IN,  * 
FROM dbo.MASTER_POLICY MP
JOIN dbo.MASTER_POLICY_ASSIGNMENT MPA ON MPA.MASTER_POLICY_ID = MP.ID
JOIN dbo.MASTER_POLICY_ENDORSEMENT MPE ON MPE.MASTER_POLICY_ASSIGNMENT_ID = MPA.ID
JOIN dbo.POLICY_ENDORSEMENT pe ON pe.ID = MPE.POLICY_ENDORSEMENT_ID
JOIN dbo.LENDER L ON mp.LENDER_ID = L. ID
WHERE MPE.PURGE_DT IS NULL AND PE.PURGE_DT IS NULL AND 
(PE.NAME_TX LIKE '%borrow%' OR PE.NAME_TX LIKE '%Lender%')
 AND MP.CARRIER_ID =88
GROUP BY l.NAME_TX,
 PE.NAME_TX,PE.PERCENTAGE_NO, MPE.START_DT, MPE.END_DT

