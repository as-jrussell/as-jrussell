USE UniTrac



--Loan Screen Information - LOAN/COLLATERAL/PROPERTY/OWNER/REQUIRED COVERAGE
SELECT DISTINCT L.NUMBER_TX [Loan Number], FPC.NUMBER_TX, RC.DESCRIPTION_TX [Loan Type]
, D.DESCRIPTION_TX [Record Type]
INTO jcs..INC0329446
FROM LOAN L
JOIN dbo.FORCE_PLACED_CERTIFICATE fpc ON fpc.LOAN_ID = L.ID
JOIN dbo.CPI_QUOTE CPI ON CPI.ID = fpc.CPI_QUOTE_ID
JOIN dbo.CPI_ACTIVITY A ON A.CPI_QUOTE_ID = CPI.ID
JOIN dbo.COLLATERAL C ON C.LOAN_ID = L.ID
JOIN dbo.PROPERTY P ON P.ID = C.PROPERTY_ID
JOIN dbo.REF_CODE RC ON RC.CODE_CD = L.TYPE_CD AND RC.DOMAIN_CD = 'LoanType'
JOIN dbo.REF_CODE D ON D.CODE_CD = L.RECORD_TYPE_CD AND D.DOMAIN_CD = 'RecordType'
WHERE L.LENDER_ID = 208 AND (L.RECORD_TYPE_CD = 'A' OR P.RECORD_TYPE_CD = 'A')
AND A.REASON_CD = 'CHRG_OFF'