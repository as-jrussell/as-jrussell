
-- Adds INS_Reason and LN_PredDec to CPIINFB report, and sets LN_PredDec default to false.
-- 




IF NOT EXISTS (SELECT * FROM REF_CODE_ATTRIBUTE WHERE ATTRIBUTE_CD = 'INS_Reason' AND REF_CD = 'CPIINFB' AND DOMAIN_CD = 'Report_CPIPolicyStatus')
INSERT INTO REF_CODE_ATTRIBUTE (ATTRIBUTE_CD,REF_CD,DOMAIN_CD,VALUE_TX,CREATE_DT,UPDATE_DT,UPDATE_USER_TX,LOCK_ID,AGENCY_ID) 
Values ('INS_Reason','CPIINFB','Report_CPIPolicyStatus','T',GETDATE(),GETDATE(),'INC0347550',1,0)


IF NOT EXISTS (SELECT * FROM REF_CODE_ATTRIBUTE WHERE ATTRIBUTE_CD = 'LN_PredDec' AND REF_CD = 'DEFAULT' AND DOMAIN_CD = 'Report_CPIPolicyStatus')
