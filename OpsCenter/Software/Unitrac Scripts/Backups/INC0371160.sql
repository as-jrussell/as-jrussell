use UniTrac


--DROP TABLE jcs..INC0371160_Step1
SELECT L.NUMBER_TX, LL.CODE_TX, OL.OWNER_TYPE_CD, O.*
--into UnitracHDStorage..INC0371516
 FROM LOAN L
INNER JOIN OWNER_LOAN_RELATE OL ON L.ID = OL.LOAN_ID
INNER JOIN OWNER O ON OL.OWNER_ID = O.ID
INNER JOIN OWNER_ADDRESS OA ON O.ADDRESS_ID = OA.ID
INNER JOIN dbo.LENDER LL ON LL.ID = L.LENDER_ID
WHERE LL.CODE_TX != '2530' and  --L.NUMBER_TX = '165335-1' and OL.OWNER_TYPE_CD = 'CS'
 O.ID IN (select id from UnitracHDStorage..INC0371516)


update O
set O.purge_dt = GETDATE(), O.LOCK_ID = CASE WHEN O.LOCK_ID >= 255 THEN 1 ELSE O.LOCK_ID + 1 END, O.UPDATE_DT = GETDATE(), O.UPDATE_USER_TX = 'INC0371516'
--select OWNER_TYPE_CD, *
from OWNER O 
join UnitracHDStorage..INC0371516 OO on O.ID = OO.ID 

