USE UniTrac

SELECT UMR.LOAN_ID
INTO #tmp
 FROM LOAN L 
INNER JOIN UTL_MATCH_RESULT UMR 
ON UMR.UTL_LOAN_ID = L.ID 
WHERE L.CREATE_DT >= '2017-07-01' 
AND L.RECORD_TYPE_CD = 'E' 
AND L.PURGE_DT IS NOT NULL 

SELECT LE.CODE_TX, L.NUMBER_TX, WI.ID,WI.CREATE_DT, WI.STATUS_CD 
INTO jcs..INC0306871
FROM WORK_ITEM WI 
JOIN REQUIRED_COVERAGE RC ON RC.ID = WI.RELATE_ID 
JOIN PROPERTY P ON P.ID = RC.PROPERTY_ID AND P.PURGE_DT IS NULL 
JOIN COLLATERAL C ON C.PROPERTY_ID = P.ID AND C.PURGE_DT IS NULL 
JOIN LOAN L ON L.ID = C.LOAN_ID AND L.PURGE_DT IS NULL 
JOIN LENDER LE ON LE.ID = L.LENDER_ID 
JOIN PROPERTY_OWNER_POLICY_RELATE POPR ON POPR.PROPERTY_ID = P.ID 
JOIN OWNER_POLICY OP ON OP.ID = POPR.OWNER_POLICY_ID 
WHERE L.ID in (SELECT * FROM #tmp) 
AND WI.CREATE_DT >= '2017-07-01' 
AND WI.STATUS_CD NOT IN('WITHDRAWN','COMPLETE') 
AND OP.MOST_RECENT_TXN_TYPE_CD ='NBS' 
Order by wi.create_dt 
