SELECT *
FROM AGENCY
WHERE NAME_TX LIKE '%Safe%'

SELECT *
FROM RELATED_DATA 
WHERE VALUE_TX = '6608'

SELECT
rdd.NAME_TX
, rdd.DESC_TX
, rdd.RELATE_CLASS_NM
, a.NAME_TX
, a.SHORT_NAME_TX
, rd.VALUE_TX, d.*
FROM RELATED_DATA rd
JOIN AGENCY a ON a.ID = rd.RELATE_ID
JOIN RELATED_DATA_DEF rdd ON rd.DEF_ID = rdd.ID
JOIN dbo.RELATED_DATA_DEF D ON D.ID = DEF_ID
WHERE VALUE_TX = '014700'


