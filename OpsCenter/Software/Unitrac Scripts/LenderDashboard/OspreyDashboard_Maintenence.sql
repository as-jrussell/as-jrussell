--You can also query against PROCESS_LOG in teh dashboard DB to find out how long it ran as well:

Select *
from OspreyDashboard.dbo.PROCESS_LOG
WHERE CAST(START_DT AS DATE) >= CAST(GETDATE()-7 AS DATE)
ORDER BY START_DT DESC


