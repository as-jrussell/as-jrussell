USE ROLE ACCOUNTADMIN;

BEGIN
	CASE WHEN (select COUNT(*) FROM information_schema.ENABLED_ROLES where NAME = 'TEMP_ROLE') = 0
	THEN

CREATE ROLE TEMP_ROLE;
RETURN 'SUCCESS: Role created';

ELSE 

RETURN 'WARNING: Role exists';


	END;
    
END;



GRANT ROLE IDENTIFIER('"TEMP_ROLE"') TO ROLE IDENTIFIER('"SYSADMIN"');