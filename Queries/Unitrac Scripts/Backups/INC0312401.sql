USE UniTrac

SELECT RC.ID INTO #tmpRC FROM dbo.PROPERTY P
JOIN dbo.REQUIRED_COVERAGE RC ON RC.PROPERTY_ID = P.ID
JOIN dbo.LENDER L ON L.ID = P.LENDER_ID
WHERE L.CODE_TX = '3124'



SELECT PC.ENTITY_ID INTO #tmpC FROM dbo.PROPERTY_CHANGE pc
LEFT JOIN dbo.PROPERTY_CHANGE_UPDATE pcu ON pc.id = pcu.CHANGE_ID
WHERE  PC.ENTITY_NAME_TX = 'Allied.UniTrac.RequiredCoverage' AND PC.ENTITY_ID IN (SELECT * FROM #tmpRC) AND
PC.USER_TX IN ('rchhin','tlofton') AND pcu.TO_VALUE_TX = 'T'


USE [UniTrac]
GO 

--Loan Screen Information - LOAN/COLLATERAL/PROPERTY/OWNER/REQUIRED COVERAGE
SELECT L.* INTO JCS..INC0312401 FROM LOAN L
INNER JOIN COLLATERAL C ON L.ID = C.LOAN_ID
INNER JOIN PROPERTY P ON C.PROPERTY_ID = P.ID
INNER JOIN REQUIRED_COVERAGE RC ON P.ID = RC.PROPERTY_ID
INNER JOIN OWNER_LOAN_RELATE OL ON L.ID = OL.LOAN_ID
INNER JOIN OWNER O ON OL.OWNER_ID = O.ID
INNER JOIN OWNER_ADDRESS OA ON O.ADDRESS_ID = OA.ID
INNER JOIN dbo.LENDER LL ON LL.ID = L.LENDER_ID
WHERE RC.ID IN (SELECT * FROM #tmpC)


