USE UniTrac

SELECT DISTINCT TP.EXTERNAL_ID_TX [Lender Code] ,
        TP.NAME_TX [Lender Name] INTO Jcs..INC0286490_FTP
      FROM dbo.TRADING_PARTNER TP
JOIN dbo.DELIVERY_INFO DI ON DI.TRADING_PARTNER_ID = TP.ID
JOIN dbo.DELIVERY_INFO_GROUP DIG ON DIG.DELIVERY_INFO_ID = DI.ID
JOIN dbo.DELIVERY_DETAIL DD ON DD.DELIVERY_INFO_GROUP_ID = DIG.ID
JOIN dbo.LENDER L ON L.CODE_TX = TP.EXTERNAL_ID_TX
WHERE DD.VALUE_TX LIKE '%ftp%'
AND DELIVERY_CODE_TX  = 'InputFolder'
 AND L.TEST_IN ='N' AND L.STATUS_CD = 'ACTIVE' AND L.CANCEL_DT IS NULL
AND TP.PURGE_DT IS NULL AND L.PURGE_DT IS NULL 
ORDER BY TP.EXTERNAL_ID_TX ASC 
