--Loan Screen Information - LOAN/COLLATERAL/PROPERTY/OWNER/REQUIRED COVERAGE
 SELECT
	DISTINCT
        ih.SPECIAL_HANDLING_XML.value('(/SH/Status)[1]', 'varchar (50)') [CPI] ,
        L.NUMBER_TX [Loan Number] ,
        RC2.DESCRIPTION_TX [Loan Record Type] ,
        RC3.DESCRIPTION_TX [Loan Status] ,
        RC4.DESCRIPTION_TX [Loan Type] ,
        C.COLLATERAL_NUMBER_NO [Coll Nbr] ,
        O.LAST_NAME_TX ,
        O.FIRST_NAME_TX ,
        L.EFFECTIVE_DT [Contract Effective Date] ,
        L.BALANCE_AMOUNT_NO [Contract Balance] ,
        OA.LINE_1_TX ,
        ih.SPECIAL_HANDLING_XML.value('(/SH/PolicyNo)[1]', 'varchar (50)') [Policy Number] ,
        IH.EFFECTIVE_DT [Date CPI Effective] 
--SELECT COUNT(*) 
INTO JCs..INC0246263
FROM    LOAN L
        INNER JOIN COLLATERAL C ON L.ID = C.LOAN_ID
        INNER JOIN PROPERTY P ON C.PROPERTY_ID = P.ID
        INNER JOIN dbo.LENDER LL ON LL.ID = L.LENDER_ID
		INNER JOIN dbo.NOTICE N ON N.LOAN_ID = L.ID
        INNER JOIN dbo.INTERACTION_HISTORY IH ON IH.PROPERTY_ID = c.PROPERTY_ID
        INNER JOIN dbo.OWNER_LOAN_RELATE OLR ON OLR.LOAN_ID = L.ID
        INNER JOIN dbo.OWNER O ON O.ID = OLR.OWNER_ID
        INNER JOIN dbo.OWNER_ADDRESS OA ON OA.ID = P.ADDRESS_ID
        INNER JOIN dbo.REF_CODE RC2 ON RC2.CODE_CD = L.RECORD_TYPE_CD
                                       AND RC2.DOMAIN_CD = 'RecordType'
        INNER JOIN dbo.REF_CODE RC3 ON RC3.CODE_CD = L.STATUS_CD
                                       AND RC3.DOMAIN_CD = 'LoanStatus'
        INNER JOIN dbo.REF_CODE RC4 ON RC4.CODE_CD = L.TYPE_CD
                                       AND RC4.DOMAIN_CD = 'LoanType'
WHERE   LL.CODE_TX IN ( '6035' ) AND L.TYPE_CD = 'EQ'
        AND ih.SPECIAL_HANDLING_XML.value('(/SH/Status)[1]', 'varchar (50)') LIKE ( '%CPI%' )
ORDER BY IH.EFFECTIVE_DT ASC

