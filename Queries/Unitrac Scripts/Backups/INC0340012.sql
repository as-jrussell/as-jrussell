USE UniTrac



SELECT U.USER_NAME_TX,  WI.CONTENT_XML.value('(/Content/Lender/Code)[1]', 'varchar (50)') Lender,
WI.CONTENT_XML.value('(/Content/Information/ProcessLogs/ProcessLog/@Id)[1]', 'varchar (50)') ProcessID,
WI.CONTENT_XML.value('(/Content/Information/ProcessLogs/ProcessLog/@Id)[2]', 'varchar (50)') ProcessID,
L.NAME_TX,
WI.* FROM dbo.WORK_ITEM WI
LEFT JOIN dbo.LENDER L ON L.ID = WI.LENDER_ID
LEFT JOIN dbo.USERS U ON U.ID = WI.CHECKED_OUT_OWNER_ID
WHERE WI.ID IN ( 43934713 , 43934324)




SELECT PLI.RELATE_ID INTO #tmpPLI FROM dbo.PROCESS_LOG_ITEM PLI
WHERE PLI.PROCESS_LOG_ID IN (56439979,
56440292) AND PLI.RELATE_TYPE_CD = 'Allied.UniTrac.Loan'




SELECT P.ADDRESS_ID INTO #tmpP FROM LOAN L
INNER JOIN COLLATERAL C ON L.ID = C.LOAN_ID
INNER JOIN PROPERTY P ON C.PROPERTY_ID = P.ID
INNER JOIN REQUIRED_COVERAGE RC ON P.ID = RC.PROPERTY_ID
INNER JOIN OWNER_LOAN_RELATE OL ON L.ID = OL.LOAN_ID
INNER JOIN OWNER O ON OL.OWNER_ID = O.ID
INNER JOIN OWNER_ADDRESS OA ON O.ADDRESS_ID = OA.ID
INNER JOIN dbo.LENDER LL ON LL.ID = L.LENDER_ID
WHERE L.LENDER_ID IN (34)  AND L.ID IN (SELECT * FROM #tmpPLI)



UPDATE OA
SET    OA.LINE_2_TX = NULL
--SELECT * 
FROM   dbo.OWNER_ADDRESS OA
WHERE OA.ID IN (SELECT * FROM #tmpP)
AND OA.LINE_1_TX = OA.LINE_2_TX

