USE UniTrac

SELECT MESSAGE_ID INTO #tmp FROM dbo.TRADING_PARTNER_LOG
WHERE LOG_MESSAGE LIKE '%bodyWithStateAndIndex%'
AND PROCESS_CD = 'MS' 




SELECT * FROM dbo.TRADING_PARTNER_LOG
WHERE MESSAGE_Id IN (SELECT * FROM #tmp) AND TRADING_PARTNER_LOG.MESSAGE_ID NOT IN (6030303)
ORDER BY TRADING_PARTNER_LOG.MESSAGE_ID, CREATE_DT ASC


SELECT * FROM dbo.WORK_ITEM
WHERE RELATE_ID IN (SELECT DISTINCT MESSAGE_ID FROM #tmp)
AND WORKFLOW_DEFINITION_ID = '1'