SELECT * FROM dbo.PROCESS_LOG_ITEM
WHERE RELATE_ID = '48156914'
AND RELATE_TYPE_CD LIKE '%.RequiredCoverage'



SELECT CANCELLATION_DT, OP.UPDATE_DT, * FROM dbo.POLICY_COVERAGE PC 
JOIN dbo.OWNER_POLICY OP ON OP.ID = PC.OWNER_POLICY_ID
JOIN dbo.PROPERTY_OWNER_POLICY_RELATE POP ON POP.OWNER_POLICY_ID = OP.ID
JOIN dbo.PROPERTY P ON P.ID = POP.PROPERTY_ID
JOIN dbo.REQUIRED_COVERAGE RC ON RC.PROPERTY_ID = P.ID
WHERE RC.ID = '48156914'