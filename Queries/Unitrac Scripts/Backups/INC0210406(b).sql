SELECT DISTINCT C.COLLATERAL_CODE_ID, CODE_TX, NUMBER_TX, L.STATUS_CD
      
FROM    LOAN L
        INNER JOIN COLLATERAL C ON L.ID = C.LOAN_ID
        INNER JOIN PROPERTY P ON C.PROPERTY_ID = P.ID
        INNER JOIN REQUIRED_COVERAGE RC ON P.ID = RC.PROPERTY_ID
        INNER JOIN OWNER_LOAN_RELATE OL ON L.ID = OL.LOAN_ID
        INNER JOIN OWNER O ON OL.OWNER_ID = O.ID
        INNER JOIN OWNER_ADDRESS OA ON O.ADDRESS_ID = OA.ID
        INNER JOIN dbo.COLLATERAL_CODE CC ON CC.ID = C.COLLATERAL_CODE_ID
        INNER JOIN dbo.LCCG_COLLATERAL_CODE_RELATE LCG ON LCG.COLLATERAL_CODE_ID = CC.ID
        INNER JOIN dbo.LENDER_COLLATERAL_CODE_GROUP LL ON LL.ID = LCG.ID
WHERE   L.LENDER_ID IN (10 )
        AND C.COLLATERAL_CODE_ID IN ( 2, 7, 8, 9, 10, 11,12,14, 15, 16, 29, 39, 40,41,61,62,63,229)
		AND L.STATUS_CD = 'A'
		ORDER BY C.COLLATERAL_CODE_ID DESC




