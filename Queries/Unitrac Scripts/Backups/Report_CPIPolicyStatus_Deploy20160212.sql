	CREATE Table #tmpMultiCPI
	(
	[LOAN_NUMBER_TX] [nvarchar](18) NULL,
	[INSCOMPANY_POLICY_NO] [nvarchar](18) NULL,
	[LOAN_LENDERCODE_TX] [nvarchar](10) NULL,
