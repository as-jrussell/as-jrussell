USE UniTrac 
---Finding Collateral Code
SELECT TOP 1 CC.ID ,
        CC.CODE_TX ,
        CC.DESCRIPTION_TX, SECONDARY_CLASS_CD, CC.*
         FROM dbo.COLLATERAL_CODE CC
INNER JOIN dbo.LCCG_COLLATERAL_CODE_RELATE CCR ON CCR.COLLATERAL_CODE_ID = CC.ID
INNER JOIN dbo.LENDER_COLLATERAL_CODE_GROUP LCCG ON LCCG.ID = CCR.LCCG_ID
INNER JOIN dbo.LENDER L ON L.ID = LCCG.LENDER_ID
WHERE CC.CODE_TX like '%AIR%'



SELECT DISTINCT CC.CODE_TX [Collateral Code], L.CODE_TX [Lender Code], L.NAME_TX [Lender Name]
         FROM dbo.COLLATERAL_CODE CC
INNER JOIN dbo.LCCG_COLLATERAL_CODE_RELATE CCR ON CCR.COLLATERAL_CODE_ID = CC.ID
INNER JOIN dbo.LENDER_COLLATERAL_CODE_GROUP LCCG ON LCCG.ID = CCR.LCCG_ID
INNER JOIN dbo.LENDER L ON L.ID = LCCG.LENDER_ID
WHERE CC.CODE_TX like '%AIR%' AND L.STATUS_CD = 'ACTIVE' AND L.PURGE_DT IS NULL
AND L.TEST_IN <> 'Y' AND CC.PURGE_DT IS NULL AND CCR.PURGE_DT IS NULL AND LCCG.PURGE_DT IS NULL
ORDER BY L.CODE_TX ASC 