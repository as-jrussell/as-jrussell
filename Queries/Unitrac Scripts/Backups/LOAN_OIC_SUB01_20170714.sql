CREATE VIEW [dbo].[LOAN_OIC]
AS

SELECT LOAN.NUMBER_TX, LOAN.LENDER_ID,LOAN.EFFECTIVE_DT,COLLATERAL_CODE.CONTRACT_TYPE_CD,COLLATERAL_CODE.CODE_TX
FROM LOAN
INNER JOIN COLLATERAL ON LOAN.ID = COLLATERAL.LOAN_ID  and COLLATERAL.PRIMARY_LOAN_IN = 'Y'
INNER JOIN COLLATERAL_CODE ON COLLATERAL.COLLATERAL_CODE_ID = COLLATERAL_CODE.ID


GO

