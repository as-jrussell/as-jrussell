--Loan Screen Information - LOAN/COLLATERAL/PROPERTY/OWNER/REQUIRED COVERAGE
SELECT DISTINCT  L.NUMBER_TX, RC.TYPE_CD, RCC.DESCRIPTION_TX, 
OP.EFFECTIVE_DT, OP.CANCELLATION_DT, OP.CANCEL_REASON_CD
--INTO UniTracHDStorage..AAINC0230571
FROM LOAN L
INNER JOIN COLLATERAL C ON L.ID = C.LOAN_ID
INNER JOIN PROPERTY P ON C.PROPERTY_ID = P.ID
INNER JOIN REQUIRED_COVERAGE RC ON P.ID = RC.PROPERTY_ID
INNER JOIN OWNER_LOAN_RELATE OL ON L.ID = OL.LOAN_ID
INNER JOIN OWNER O ON OL.OWNER_ID = O.ID
INNER JOIN OWNER_ADDRESS OA ON O.ADDRESS_ID = OA.ID
INNER JOIN dbo.LENDER LL ON LL.ID = L.LENDER_ID
INNER JOIN dbo.PROPERTY_OWNER_POLICY_RELATE POP ON POP.PROPERTY_ID = P.ID
INNER JOIN dbo.OWNER_POLICY OP ON OP.ID = POP.OWNER_POLICY_ID
INNER JOIN dbo.POLICY_COVERAGE PC ON PC.OWNER_POLICY_ID = OP.ID
INNER JOIN dbo.REF_CODE RCC ON RCC.CODE_CD = OP.STATUS_CD AND RCC.DOMAIN_CD = 'OwnerPolicyStatus'
WHERE LL.CODE_TX IN ('7150') AND OP.STATUS_CD NOT IN ('F', 'P') AND DIVISION_CODE_TX = '99'
 AND RC.TYPE_CD LIKE 'hazard' 



SELECT * FROM dbo.REF_CODE
WHERE DOMAIN_CD = 'OwnerPolicyStatus'



SELECT LO.TYPE_CD, LO.CODE_TX , Lo.NAME_TX,*
FROM LENDER L
INNER JOIN LENDER_ORGANIZATION LO ON L.ID = LO.LENDER_ID
INNER JOIN RELATED_DATA RD ON LO.ID = RD.RELATE_ID --AND RD.DEF_ID = '105'
WHERE L.CODE_TX = '7150' AND Lo.TYPE_CD = 'DIV'

