USE UniTrac	


SELECT L.NUMBER_TX, l.DIVISION_CODE_TX, RC.* 
--INTO UniTracHDStorage..INC0273888
FROM LOAN L
INNER JOIN COLLATERAL C ON L.ID = C.LOAN_ID
INNER JOIN PROPERTY P ON C.PROPERTY_ID = P.ID
INNER JOIN REQUIRED_COVERAGE RC ON P.ID = RC.PROPERTY_ID
INNER JOIN OWNER_LOAN_RELATE OL ON L.ID = OL.LOAN_ID
INNER JOIN OWNER O ON OL.OWNER_ID = O.ID
INNER JOIN OWNER_ADDRESS OA ON O.ADDRESS_ID = OA.ID
INNER JOIN dbo.LENDER LL ON LL.ID = L.LENDER_ID
WHERE LL.CODE_TX IN ('4260') AND RC.SUMMARY_STATUS_CD = 'A' 
AND L.RECORD_TYPE_CD = 'G' AND RC.PURGE_DT IS NULL
AND RC.RECORD_TYPE_CD = 'G' AND RC.TYPE_CD <> 'PHYS-DAMAGE'




UPDATE RC
SET UPDATE_DT = GETDATE(), UPDATE_USER_TX = 'INC0273888', LOCK_ID = LOCK_ID+1,
EXPOSURE_DT = '2016-08-01'
--SELECT * 
FROM dbo.REQUIRED_COVERAGE RC 
WHERE ID IN (SELECT ID FROM UniTracHDStorage..INC0273888)


INSERT INTO PROPERTY_CHANGE
 ( ENTITY_NAME_TX , ENTITY_ID , USER_TX , ATTACHMENT_IN , 
 CREATE_DT , AGENCY_ID , DESCRIPTION_TX ,  DETAILS_IN , FORMATTED_IN ,
 LOCK_ID , PARENT_NAME_TX , PARENT_ID , TRANS_STATUS_CD , UTL_IN )
 SELECT DISTINCT 'Allied.UniTrac.RequiredCoverage' , ID , 'INC0273888' , 'N' , 
 GETDATE() ,  1 , 
'Set Exposure Date to 8/1/2016', 
 'Y' , 'N' , 1 ,  'Allied.UniTrac.RequiredCoverage' , ID , 'PEND' , 'N'
FROM dbo.REQUIRED_COVERAGE
WHERE ID IN (SELECT ID FROM UniTracHDStorage..INC0273888)


