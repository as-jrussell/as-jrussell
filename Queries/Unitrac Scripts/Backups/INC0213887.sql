SELECT DISTINCT C.LOAN_ID, L.NUMBER_TX, RC.INSURANCE_SUB_STATUS_CD, RC.SUMMARY_STATUS_CD
FROM    LOAN L
        INNER JOIN COLLATERAL C ON L.ID = C.LOAN_ID
        INNER JOIN PROPERTY P ON C.PROPERTY_ID = P.ID
        INNER JOIN REQUIRED_COVERAGE RC ON P.ID = RC.PROPERTY_ID
        INNER JOIN OWNER_LOAN_RELATE OL ON L.ID = OL.LOAN_ID
        INNER JOIN OWNER O ON OL.OWNER_ID = O.ID
        INNER JOIN OWNER_ADDRESS OA ON O.ADDRESS_ID = OA.ID
		INNER JOIN dbo.LENDER_PRODUCT LP ON LP.ID = RC.LENDER_PRODUCT_ID
		INNER JOIN dbo.LENDER_PRODUCT_CHANGE LPC ON LPC.ID = LP.ID
WHERE   L.LENDER_ID IN ( 2254 )
        AND RC.ID IN ( SELECT ID FROM UniTracHDStorage..OnPointINC0213887) AND 
		INSURANCE_SUB_STATUS_CD <> 'D' AND SUMMARY_STATUS_CD <> 'F'  AND COLLATERAL_CODE_ID IN 
		(SELECT ID FROM dbo.COLLATERAL_CODE WHERE VEHICLE_LOOKUP_IN = 'N')


