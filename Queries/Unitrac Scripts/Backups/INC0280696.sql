SELECT  L.PURGE_DT [Loan Purge] ,
        RC1.DESCRIPTION_TX [Loan Status] ,
        P.PURGE_DT [Property Purge] ,
        RC2.DESCRIPTION_TX [Property Status] ,
        C.PURGE_DT [Collateral Purge] ,
       RC3.DESCRIPTION_TX [Required Coverage Status] ,
        RC.PURGE_DT [Required Coverage Purge]
FROM    LOAN L
        INNER JOIN COLLATERAL C ON L.ID = C.LOAN_ID
        INNER JOIN PROPERTY P ON C.PROPERTY_ID = P.ID
        INNER JOIN REQUIRED_COVERAGE RC ON P.ID = RC.PROPERTY_ID
        INNER JOIN OWNER_LOAN_RELATE OL ON L.ID = OL.LOAN_ID
        INNER JOIN OWNER O ON OL.OWNER_ID = O.ID
        INNER JOIN OWNER_ADDRESS OA ON O.ADDRESS_ID = OA.ID
        INNER JOIN dbo.LENDER LL ON LL.ID = L.LENDER_ID
        INNER JOIN dbo.REF_CODE RC1 ON RC1.CODE_CD = L.RECORD_TYPE_CD
                                       AND RC1.DOMAIN_CD = 'RecordType'
        INNER JOIN dbo.REF_CODE RC2 ON RC2.CODE_CD = P.RECORD_TYPE_CD
                                       AND RC2.DOMAIN_CD = 'RecordType'
        INNER JOIN dbo.REF_CODE RC3 ON RC3.CODE_CD = RC.RECORD_TYPE_CD
                                    AND RC3.DOMAIN_CD = 'RecordType'
WHERE   LL.CODE_TX IN ( '7150' )
        AND L.NUMBER_TX IN ( '616029' )

