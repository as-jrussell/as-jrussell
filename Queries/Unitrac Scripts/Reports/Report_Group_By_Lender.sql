USE UniTrac


SELECT L.CODE_TX, COUNT(RH.ID) FROM dbo.REPORT_HISTORY RH
JOIN dbo.LENDER L ON L.ID = RH.LENDER_ID
WHERE RH.REPORT_ID NOT IN (48, 54, 35,38,47,71,32,37,46,50,51,52,53,69,58,68,80)
AND RH.STATUS_CD = 'PEND'
GROUP BY L.CODE_TX
ORDER BY COUNT(RH.ID) DESC  


